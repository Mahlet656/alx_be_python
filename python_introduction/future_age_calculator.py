# Prompt the user for their current age and convert it to an integer
current_age = int(input("How old are you? "))

# Calculate the user's age in 2050 (assuming current year is 2023, so add 27 years)
future_age = current_age + 27

# Print the user's future age
print(f"In 2050, you will be {future_age} years old.")
